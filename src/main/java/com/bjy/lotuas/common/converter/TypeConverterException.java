package com.bjy.lotuas.common.converter;

public class TypeConverterException extends Exception {

	private static final long serialVersionUID = -2388332991742597749L;

	public TypeConverterException() {
		super();
	}
	
	public TypeConverterException(String message, Throwable cause) {
		super(message, cause);
	}

	public TypeConverterException(String message) {
		super(message);
	}

	public TypeConverterException(Throwable cause) {
		super(cause);
	}
}
