package com.bjy.lotuas.common.excel;

public class DefExcelException extends Exception {
	private static final long serialVersionUID = 346873925852583451L;

	public DefExcelException() {
		super();
	}

	public DefExcelException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public DefExcelException(String arg0) {
		super(arg0);
	}

	public DefExcelException(Throwable arg0) {
		super(arg0);
	}
    
	
}
